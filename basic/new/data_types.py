
# # # There are two types of datatypes in python: 

# # # 1. immutable :- values can't be change   
# # # 2. mutable :- values can be change 

# # # immutable :-- string,int,complex,float,tuple,boolean,range,Bytearray,frozenset

# # # mutable :-- list,dict,set,array


# # # string: It is a sequence of characters. It is immutable.

# # x = "This is my car"
# # y = "mello is demo"


# # int: It is a whole number, positive or negative, without decimals, of unlimited length.

# # x = 1
# # y = 35656222554887711
# # z = -3255522

# # float: It is a number, positive or negative, containing one or more decimals.

# # x = 1.10
# # y = 1.0
# # z = -35.59  


# # complex: It is a complex number, composed of a real, and an imaginary part.

# # x = 3+5j
# # y = 5j
# # z = -5j

# # tuple : It is an ordered sequence of immutable elements. It is immutable.

# # x = (5, 10, 15, 20)
# # y = (5, 10, 15, 20, 25)
# # z = "hello", "world", "python"

# Boolean : It is a data type with two values: True or False.

# # x = True
# # y = False


# # range : It is a sequence of numbers, which starts from 0 by default, and increments by 1 (by default), and ends at a specified number.



#  x = range(6)


# there are three types of numbers 

# x = 1; # int
# y = 2.8; # float
# z = 1j; # complex



# List : It is a collection which is ordered and changeable. Allows duplicate members.

# x = ["apple", "banana", "cherry"]
# y = [1, 5, 7, 9, 3]
# z = [True, False, False]

# Dictionary : It is an unordered collection of data values, used to store data values like a map, which unlike other Data Types that hold only a single value as an element, Dictionary holds key : value pair.
# x = {"name": "John", "age": 36}

# Set : It is an unordered collection of unique elements. No duplicate members.

# x = {"apple", "banana", "cherry"}